function seriesLabelFor(row, ix) {
  const raw = row[ix.series] ?? '';
  if (raw && SERIES_LABELS[raw]) return SERIES_LABELS[raw];
  const slug = row[ix.slug] ?? '';
  if (/gorod-geroy/.test(slug)) return 'Города-герои';
  if (/-krym|-sevastopol|krymsk/.test(slug)) return 'Крымские события';
  return '';
}

/**
 * Подпись номинала по-русски: «1 копейка / 5 копеек / 1 рубль / 2 рубля /
 * 5 рублей / 10 рублей». Нужна в нескольких шаблонах.
 */
function formatDenom(c) {
  const d = c.denom;
  if (c.unit === 'копейка') {
    if (d === 1) return '1 копейка';
    return `${d} копеек`;
  }
  if (d === 1) return '1 рубль';
  if (d >= 2 && d <= 4) return `${d} рубля`;
  return `${d} рублей`;
}

/** Собирает объект c нужными для правил полями. */
function toCoin(row, ix) {
  const priceCols = ['цена_vf20', 'цена_ef40', 'цена_au50', 'цена_ms63', 'цена_ms65'];
  const prices = priceCols.map((k) => Number(row[ix[k]] || 0));
  const maxPrice = Math.max(0, ...prices);
  const hasPrice = maxPrice > 0;
  return {
    slug: row[ix.slug],
    name: row[ix['название']] ?? '',
    year: Number(row[ix['год']]),
    denom: Number(row[ix['номинал']]),
    unit: row[ix['единица_номинала']],
    mint: row[ix['монетный_двор']],
    type: row[ix.type],
    material: row[ix.material],
    variant: row[ix['разновидность']] ?? '',
    mintage: Number(row[ix['тираж']] || 0),
    series: row[ix.series] ?? '',
    seriesLabel: seriesLabelFor(row, ix),
    hasPrice,
    maxPrice
  };
}

