function isKnownTemplate(text) {
  const t = (text ?? '').trim();
  if (!t) return true;
  if (KNOWN_STATIC_TEXTS.has(t)) return true;
  if (KNOWN_PREFIXES.some((p) => t.startsWith(p))) return true;

  // Новый ordinary_rf выдаёт тексты вида «1 копейка 1997 М — ...» / «10
  // копеек 2006 М — редкий «магнитный» вариант...». Ловим их по характерной
  // паре «<номинал> <год> <двор> —» в начале + один из маркерных оборотов.
  // Шаблонные тексты регулярных монет — формат «1 рубль 1991 ЛМД —» ИЛИ
  // «10 копеек 1991 года со знаком …» + характерные маркёры словаря.
  if (/^\d+\s+(копейка|копеек|рубль|рубля|рублей)\s+(?:\d{4}\s+года\s+\S+|\d{4}\s+\S{1,4})/.test(t)
      && /(немагнитн|магнитн|мельхиоров|стальн|латунн|переходного года|переходный\s+металл|самый младший номинал|«жёлтая» стальная|никелевым гальваническим|латунным гальваническим|поздний выпуск|рядовая монета регулярного|сверхредкий экземпляр|нечастая разновидность|ГКЧП|перепутк|знаменитая\s+разновидност|разменная\s+монета|биметаллическ|медно-никелев|ленинградский\s+тираж|московский\s+тираж|старшего?\s+номинал|младший\s+номинал|«эмк|советский\s+штемпел|со\s+знаком|последняя\s+советская|последний\s+год\s+инфляционной)/i.test(t)) {
    return true;
  }

  // CBR-технические описания реверса: "изображение герба …", "изображение
  // командира …", «в центре монеты …» — это пересказ картинки, а не рассказ
  // о монете. Считаем их шаблонными, чтобы --refresh заменил на содержательный
  // текст серии.
  const low = t.toLowerCase();
  if (
    /^(изображение|в\s+центре\s+монеты|в\s+центре\s+диска|справа[\s,—-]|слева[\s,—-]|на\s+первом\s+плане|в\s+верхней\s+части)/.test(low)
  ) return true;
  // Короткие подписи-лозунги вида «Город воинской славы — Белгород. Место
  // Прохоровского танкового сражения 1943 года.» формально уникальны, но по
  // сути это тоже просто название с аверса + одна строка. Будем заменять на
  // нормальный текст серии только если описание совсем короткое (<120 симв)
  // и совпадает по смыслу с названием монеты.
  if (t.length < 120 && /город\s+воинской\s+славы|древний\s+город|столиц[аы]/i.test(t)) return true;

  return false;
}

