function parseCsv(raw) {
  const rows = []; let i = 0, f = '', row = [], q = false;
  while (i < raw.length) { const ch = raw[i];
    if (q) { if (ch === '"') { if (raw[i + 1] === '"') { f += '"'; i += 2; continue; } q = false; i++; continue; } f += ch; i++; continue; }
    if (ch === '"') { q = true; i++; continue; }
    if (ch === ',') { row.push(f); f = ''; i++; continue; }
    if (ch === '\n' || ch === '\r') { if (f.length || row.length) { row.push(f); rows.push(row); } row = []; f = ''; if (ch === '\r' && raw[i + 1] === '\n') i += 2; else i++; continue; }
    f += ch; i++;
  }
  if (f.length || row.length) { row.push(f); rows.push(row); }
  return rows;
}
function csvEsc(v) { const s = String(v ?? ''); return /[",\n]/.test(s) ? `"${s.replace(/"/g, '""')}"` : s; }

